/** Выбранный вариант параметра в строке корзины: «Размер: Большой».
 *
 *  Снимок, а не ссылка на справочник: строка корзины рисуется из
 *  localStorage мгновенно, до ответа бэкенда, и «Размер: Большой» должно
 *  быть видно сразу. valueId нужен для синка — бэкенду шлём именно его,
 *  а цену он пересчитает сам. Id самого параметра не храним: страница
 *  товара узнаёт свой выбор по valueId, а больше он никому не нужен. */
export interface CartOption {
  name: string;
  valueId: number;
  value: string;
  priceDelta: number;
}

/** Позиция корзины на клиенте.
 *
 *  Держим снимок названия/цены/фото, а не только id: корзина рисуется
 *  мгновенно из localStorage, без ожидания ответа бэкенда. Актуальную
 *  цену подтверждает бэкенд при открытии по ссылке и при оформлении
 *  заказа — там она и фиксируется. */
export interface CartLine {
  productId: number;
  slug: string;
  name: string;
  /** Цена одной штуки уже с прибавками за выбранные параметры: вся
   *  арифметика корзины (`price * quantity`) считает по ней, и добавлять
   *  прибавки в каждом месте отдельно не нужно. Базовая цена товара —
   *  `price` минус сумма priceDelta в options. */
  price: number;
  image: string | null;
  quantity: number;
  /** id категории — по нему видно, что это «букет» для подарка. */
  categoryId: number;
  /** Пусто — у товара нет параметров либо он лёг в корзину до того,
   *  как их завели. */
  options: CartOption[];
}

export interface CartState {
  lines: CartLine[];
  /** UUID корзины на бэкенде — он же ключ ссылки «поделиться».
   *  null, пока корзина ни разу не синхронизировалась. */
  serverUuid: string | null;
  /** Прочитали ли localStorage. До этого не пишем обратно и не синхроним,
   *  иначе первый же экран затёр бы сохранённую корзину пустой. */
  hydrated: boolean;
}

/** Что кладём в корзину: строка без количества (его задаёт reducer). */
export type AddToCartPayload = Omit<CartLine, 'quantity'> & {
  quantity?: number;
};
